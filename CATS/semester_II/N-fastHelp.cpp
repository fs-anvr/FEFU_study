#include <fstream>
#include <vector>
#include <algorithm>

using namespace std;

int main() {
    ifstream fin ("input.txt");
    ofstream fout ("output.txt");
    int N, L;
    fin >> N >> L;
    vector<int> a(N);
    for (int i = 0; i != N; ++i) fin >> a[i];
    sort (a.begin(), a.end());
    int sum = a[0] + L;
    for (int i = 1; i != N; ++i)
    {
        if (a[i] > sum)
        {
            sum += a[i] - sum;
        }
        sum += L;
    }
    fout << sum;
    return 0;
}
