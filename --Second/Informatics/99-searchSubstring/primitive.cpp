#include <iostream>
#include <string>

using namespace std;

int main() {
    string line, sub;
    getline(cin, line);
    getline(cin, sub);
    size_t line_len = line.size();
    size_t sub_len = sub.size();
    for (int i = sub_len - 1; i != line_len; ++i)
    {
        size_t count = 0;
        while (count != sub_len && line[i-count] == sub[sub_len-count-1])
            count++;
        if (count == sub_len)
            cout << i-sub_len+1 << ' ';
    }
    return 0;
}
