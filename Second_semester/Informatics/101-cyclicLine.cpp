#include <iostream>
#include <string>
#include <vector>

using namespace std;

vector<int> pref_func (string str);


int main()
{
    string str;
    getline(cin, str);
    vector<int> vec = pref_func(str);
    cout << vec.size()-vec[vec.size()-1];
    return 0;
}


vector<int> pref_func (string str)
{
    size_t n = str.length();
    vector<int> pref(n);
    for (int i = 1; i < n; ++i)
    {
        int j = pref[i-1];
        while (j > 0 && str[i] != str[j])
            j = pref[j-1];
        if (str[i] == str[j])  ++j;
        pref[i] = j;
    }
    return pref;
}
