#include <fstream>
#include <string>
using namespace std;
int main() {
    ifstream fin ("input.txt");
    ofstream fout ("output.txt");
    string str;
    fin >> str;
    for (int i = 1; i < str.size(); ++i)
        if (str[i-1] < 91 && str[i] > 90)
        {
            fout << "NO";
            return 0;
        }
    fout << "YES";
    fin.close();
    fout.close();
    return 0;
}
